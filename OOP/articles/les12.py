# # Паттерн MVC
# MVC — это шаблон программирования, который позволяет разделить логику
# приложения на три части:
# • Model (модель). Получает данные от контроллера, выполняет необходимые операции и передаёт их в вид.
# • View (вид или представление). Получает данные от модели и выводит их для пользователя.
# • Controller (контроллер). Обрабатывает действия пользователя, проверяет полученные данные
# и передаёт их модели.


